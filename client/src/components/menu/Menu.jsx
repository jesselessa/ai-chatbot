import "./menu.css";
import { Link } from "react-router-dom";
import { useQuery } from "@tanstack/react-query";

// Component
import Loader from "../loader/Loader.jsx";

const Menu = () => {
  // Fetch user chats
  const { isPending, error, data } = useQuery({
    queryKey: ["userChats"],
    queryFn: () =>
      fetch(`${import.meta.env.VITE_API_URL}/api/user-chats`, {
        credentials: "include",
      }).then((res) => res.json()),
  });

  return (
    <div className="menu">
      <span className="title">DASHBOARD</span>
      <Link to="/dashboard">Create a new chat</Link>
      <Link to="/">Explore Ask Jessbot</Link>
      <Link to="/">Contact</Link>

      <hr />

      <span className="title">RECENT CHATS</span>

      <div className="list">
        {isPending ? (
          <Loader width="30px" height="30px" border="3px solid transparent" />
        ) : error ? (
          <p>Something went wrong&nbsp;! Please, try again later.</p>
        ) : Array.isArray(data) ? (
          data.map((chat) => (
            <Link to={`/dashboard/chats/${chat._id}`} key={chat._id}>
              {chat.title}
            </Link>
          ))
        ) : (
          <p>No chat available.</p>
        )}
      </div>

      <hr />

      <div className="upgrade">
        <img src="/logo.png" alt="logo" />
        <div className="texts">
          <span>Ask Jessbot answers are generated by Gemini</span>
          <span>
            <a href="https://gemini.google.com/" target="_blank">
              Learn more about Google AI
            </a>
          </span>
        </div>
      </div>
    </div>
  );
};

export default Menu;
